package ru.practicum.model.event;

/**
 * Перечисление возможных вариантов сортировки событий в публичном доступе.
 * <p>
 * Используется при запросе списка событий для указания порядка сортировки результатов.
 * </p>
 *
 * @see ru.practicum.dto.event.EventPublicParam
 * @see ru.practicum.service.event.EventServiceImpl
 */
public enum EventSort {

    /**
     * Сортировка по дате проведения события.
     * <p>
     * События сортируются в хронологическом порядке по полю eventDate.
     * Используется по умолчанию, если не указан другой тип сортировки.
     * </p>
     */
    EVENT_DATE,

    /**
     * Сортировка по количеству просмотров.
     * <p>
     * События сортируются по убыванию количества просмотров.
     * Данные о просмотрах получаются из сервиса статистики.
     * </p>
     */
    VIEWS
}