package ru.practicum.model.event;

/**
 * Перечисление возможных состояний (статусов) события в процессе его жизненного цикла.
 * <p>
 * Определяет этапы, через которые проходит событие от создания до публикации или отмены.
 * Статус события влияет на доступность его для публичного просмотра и возможность
 * внесения изменений.
 * </p>
 *
 * @see ru.practicum.model.event.Event
 * @see ru.practicum.dto.event.UpdateEventAdminRequest
 * @see ru.practicum.dto.event.UpdateEventUserRequest
 */
public enum State {

    /**
     * Событие ожидает модерации.
     * <p>
     * Начальный статус для всех новых событий после создания пользователем.
     * События в этом статусе видны только автору и администраторам.
     * Могут быть отправлены на модерацию повторно после отмены.
     * </p>
     */
    PENDING,

    /**
     * Событие опубликовано.
     * <p>
     * Статус присваивается администратором после успешного прохождения модерации.
     * Опубликованные события доступны для публичного просмотра и поиска.
     * В этом статусе событие не может быть изменено пользователем.
     * </p>
     */
    PUBLISHED,

    /**
     * Событие отменено.
     * <p>
     * Статус может быть присвоен:
     * <ul>
     *   <li>пользователем - отмена события до отправки на модерацию</li>
     *   <li>администратором - отклонение события при модерации</li>
     * </ul>
     * Отмененные события не доступны для публичного просмотра,
     * но могут быть отредактированы и отправлены на модерацию повторно.
     * </p>
     */
    CANCELED
}