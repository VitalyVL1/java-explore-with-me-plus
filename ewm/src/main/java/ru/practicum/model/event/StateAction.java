package ru.practicum.model.event;

/**
 * Перечисление действий, которые могут быть выполнены над событием
 * пользователем или администратором.
 * <p>
 * Используется в запросах на обновление события для указания операции,
 * которую необходимо выполнить (публикация, отклонение, отправка на модерацию,
 * отмена модерации или отсутствие действия).
 * </p>
 *
 * @see ru.practicum.dto.event.UpdateEventAdminRequest
 * @see ru.practicum.dto.event.UpdateEventUserRequest
 */
public enum StateAction {

    /**
     * Опубликовать событие.
     * <p>
     * Действие доступно только администратору.
     * Переводит событие из статуса {@link State#PENDING} в {@link State#PUBLISHED}.
     * </p>
     */
    PUBLISH_EVENT,

    /**
     * Отклонить событие.
     * <p>
     * Действие доступно только администратору.
     * Переводит событие из статуса {@link State#PENDING} в {@link State#CANCELED}.
     * </p>
     */
    REJECT_EVENT,

    /**
     * Отправить событие на модерацию.
     * <p>
     * Действие доступно пользователю.
     * Переводит событие из статуса {@link State#CANCELED} или черновика в {@link State#PENDING}.
     * </p>
     */
    SEND_TO_REVIEW,

    /**
     * Отменить модерацию события.
     * <p>
     * Действие доступно пользователю.
     * Переводит событие из статуса {@link State#PENDING} обратно в черновик/отмену.
     * </p>
     */
    CANCEL_REVIEW,

    /**
     * Нет действия.
     * <p>
     * Используется по умолчанию, когда требуется только обновление полей
     * без изменения статуса события.
     * </p>
     */
    NO_ACTION
}