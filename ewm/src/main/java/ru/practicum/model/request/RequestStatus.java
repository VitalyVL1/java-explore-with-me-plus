package ru.practicum.model.request;

/**
 * Перечисление возможных статусов заявки на участие в событии.
 * <p>
 * Определяет жизненный цикл заявки от создания до подтверждения или отклонения.
 * Статус заявки влияет на доступность места в событии и отображение в списках участников.
 * </p>
 *
 * @see ru.practicum.model.request.Request
 * @see ru.practicum.dto.event.EventRequestStatusUpdateRequest
 */
public enum RequestStatus {

    /**
     * Заявка ожидает рассмотрения.
     * <p>
     * Начальный статус для всех новых заявок, если для события включена модерация
     * и установлен лимит участников. Заявки в этом статусе могут быть подтверждены
     * ({@link #CONFIRMED}) или отклонены ({@link #REJECTED}) инициатором события.
     * </p>
     */
    PENDING,

    /**
     * Заявка подтверждена.
     * <p>
     * Статус присваивается заявке инициатором события или автоматически,
     * если для события отключена модерация. Подтвержденные заявки гарантируют
     * участие в событии и учитываются в лимите участников.
     * </p>
     */
    CONFIRMED,

    /**
     * Заявка отклонена.
     * <p>
     * Статус присваивается инициатором события заявкам в статусе {@link #PENDING}.
     * Отклоненные заявки не участвуют в событии и не учитываются в лимите участников.
     * </p>
     */
    REJECTED,

    /**
     * Заявка отменена.
     * <p>
     * Статус присваивается самим пользователем, подавшим заявку.
     * Отмененные заявки освобождают место в событии, если они были подтверждены.
     * </p>
     */
    CANCELED
}