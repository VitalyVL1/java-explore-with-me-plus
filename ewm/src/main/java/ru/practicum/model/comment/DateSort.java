package ru.practicum.model.comment;

/**
 * Перечисление для определения направления сортировки по дате.
 * <p>
 * Используется при запросе списка комментариев для указания порядка сортировки
 * по дате создания: от старых к новым или от новых к старым.
 * </p>
 *
 * @see ru.practicum.controller.comment.AdminCommentController
 * @see ru.practicum.controller.event.PublicEventController
 * @see ru.practicum.service.comment.CommentService
 */
public enum DateSort {

    /**
     * Сортировка по возрастанию даты (от старых к новым).
     * <p>
     * Комментарии будут отсортированы в хронологическом порядке:
     * сначала более старые, затем более новые.
     * </p>
     */
    ASC,

    /**
     * Сортировка по убыванию даты (от новых к старым).
     * <p>
     * Комментарии будут отсортированы в обратном хронологическом порядке:
     * сначала самые новые, затем более старые.
     * </p>
     */
    DESC
}