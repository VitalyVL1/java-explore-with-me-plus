package ru.practicum.model.comment;

/**
 * Перечисление возможных статусов комментария в процессе модерации.
 * <p>
 * Определяет жизненный цикл комментария от создания до публикации или отклонения.
 * Комментарии проходят модерацию перед тем, как стать доступными для публичного просмотра.
 * </p>
 *
 * @see ru.practicum.model.comment.Comment
 */
public enum CommentState {

    /**
     * Комментарий ожидает модерации.
     * <p>
     * Начальный статус для всех новых комментариев.
     * Комментарии в этом статусе видны только автору и администраторам.
     * Могут быть подтверждены ({@link #APPROVED}) или отклонены ({@link #REJECTED}) администратором.
     * </p>
     */
    WAITING,

    /**
     * Комментарий подтвержден и опубликован.
     * <p>
     * Комментарии в этом статусе доступны для публичного просмотра.
     * Статус присваивается администратором при успешном прохождении модерации.
     * </p>
     */
    APPROVED,

    /**
     * Комментарий отклонен модератором.
     * <p>
     * Комментарии в этом статусе не публикуются и не видны другим пользователям.
     * Статус присваивается администратором при несоответствии правилам.
     * </p>
     */
    REJECTED
}