package ru.practicum.exception;

/**
 * Исключение, выбрасываемое при ошибках валидации данных.
 * <p>
 * Используется для обработки случаев, когда переданные данные не соответствуют
 * бизнес-правилам или техническим требованиям, которые не могут быть покрыты
 * стандартными аннотациями валидации. Например:
 * <ul>
 *   <li>некорректный диапазон дат (дата окончания раньше даты начала)</li>
 *   <li>недопустимое сочетание параметров запроса</li>
 *   <li>превышение допустимых лимитов</li>
 *   <li>некорректный формат данных для специфических бизнес-правил</li>
 * </ul>
 * </p>
 * <p>
 * Наследуется от {@link RuntimeException} и обрабатывается глобальным обработчиком
 * исключений {@link GlobalExceptionHandler} с возвратом HTTP статуса 400 (BAD_REQUEST).
 * </p>
 *
 * @see GlobalExceptionHandler
 */
public class ValidationException extends RuntimeException {

    /**
     * Создает новое исключение с указанным сообщением об ошибке.
     *
     * @param message детальное сообщение, описывающее причину исключения
     */
    public ValidationException(String message) {
        super(message);
    }
}