package ru.practicum.exception;

/**
 * Исключение, выбрасываемое при нарушении бизнес-условий выполнения операции.
 * <p>
 * Используется в случаях, когда операция не может быть выполнена из-за несоответствия
 * текущего состояния объекта требуемым условиям. Например:
 * <ul>
 *   <li>попытка публикации события, которое уже опубликовано</li>
 *   <li>попытка отмены события, которое уже подтверждено</li>
 *   <li>попытка подтверждения заявки при достижении лимита участников</li>
 *   <li>попытка редактирования события, которое уже опубликовано</li>
 * </ul>
 * </p>
 * <p>
 * Наследуется от {@link RuntimeException} и обрабатывается глобальным обработчиком
 * исключений {@link GlobalExceptionHandler} с возвратом HTTP статуса 409 (CONFLICT).
 * </p>
 *
 * @see GlobalExceptionHandler
 */
public class ConditionsNotMetException extends RuntimeException {

    /**
     * Создает новое исключение с указанным сообщением об ошибке.
     *
     * @param message детальное сообщение, описывающее причину исключения
     */
    public ConditionsNotMetException(String message) {
        super(message);
    }
}