package ru.practicum.dto;

import lombok.Builder;

/**
 * DTO (Data Transfer Object) для ответа от сервиса статистики.
 * <p>
 * Используется для получения агрегированных данных о просмотрах из сервиса статистики.
 * Содержит информацию о приложении, URI и количество просмотров (хитов) за запрошенный период.
 * </p>
 *
 * @param app идентификатор приложения, для которого собрана статистика
 * @param uri URI эндпоинта, по которому собрана статистика
 * @param hits количество просмотров (хитов) для указанного URI в запрошенном периоде
 *             (может быть общим количеством или количеством уникальных просмотров
 *             в зависимости от параметров запроса)
 */
public record ResponseStatsDto(
        String app,
        String uri,
        Long hits
) {
    /**
     * Конструктор с поддержкой паттерна Builder от Lombok.
     * Позволяет создавать объекты с использованием toBuilder().
     */
    @Builder(toBuilder = true)
    public ResponseStatsDto {
    }
}